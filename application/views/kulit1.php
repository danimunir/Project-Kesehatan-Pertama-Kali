 		<div class="text">  1. Gunakan sunscreen berbasis mineral setiap hari
	Cara menjaga kesehatan kulit yang pertama adalah gunakan sunscreen setiap hari. Radiasi ultraviolet bersifat karsinogenik. Sinar UV adalah penyebab utama perubahan tekstur dan warna penyebab penuaan kulit. Untuk melindungi kulit, direkomendasikan untuk selalu menggunakan sunscreen berbasis mineral dengan SPF 30 atau lebih.
	Gunakan setiap hari, meski Anda tidak pergi kemana-mana. Gunakan sebagai langkah terakhir dalam rutinitas perawatan kulit setiap pagi. Hal ini karena sunscreen adalah penghalang fisik, jadi harus diterapkan sebagai langkah terakhir sebelum menggunakan make up.<br><br>
	2. Konsumsi vitamin C
	Cara menjaga kesehatan kulit yang kedua adalah dengan rajin mengonsumsi vitamin C. Salah satu bahan topikal terbaik untuk kulit bercahaya, sehat, dan sederhana adalah vitamin C.
	Vitamin C bekerja pada semua jenis kulit untuk meratakan dan mencerahkan warna kulit. Vitamin C dapat memudarkan hiperpigmentasi, melindungi kulit terhadap agresor lingkungan, mencerahkan warna kulit, dan mempromosikan produksi kolagen dan elastin.<br><br>
	3. Eksfoliasi
	Cara menjaga kesehatan kulit yang ketiga adalah dengan rutin melakukan eksfoliasi sel-sel kulit mati. Lakukan eksfoliasi ringan setiap hari, dengan pembersih asam atau toner non-irritating yang melarutkan sel-sel kulit mati pada permukaan.
	Proses eksfoliasi membantu kulit tetap bersih dan bercahaya. Menghapus lapisan luar sel kulit mati akan membantu menghaluskan tekstur kulit, meningkatkan tingkat penyerapan produk perawatan kulit, dan mencerahkan kulit Anda.<br><br>
	4. Perhatikan kesehatan usus
	Cara menjaga kesehatan kulit yang ke empat adalah dengan memperhatikan kesehatan usus Anda. Penelitian menunjukkan bahwa masalah kesehatan usus dapat mendasari banyak permasalahan kulit seperti jerawat dan eksim.
	Kesehatan usus secara keseluruhan bervariasi dan kompleks. Namun penting untuk selalu diperhatikan. Luangkan waktu untuk mempelajari cara merawat dan memelihara flora usus Anda.<br><br>
	5. Konsumsi lemak sehat
	Cara menjaga kesehatan kulit yang ke lima adalah dengan rutin mengonsumsi makanan yang mengandung lemak sehat. Lemak sehat seperti yang ditemukan dalam kacang-kacangan, biji rami, dan alpukat dapat membantu mengisi kembali kemampuan tubuh untuk menciptakan membran sel yang sehat dan kuat. Hal ini dapat melindungi kulit dari polusi lingkungan dengan mengembalikan lapisan penghalang kulit.
	Kurang mengonsumsi lemak sehat dapat menyebabkan kulit dan rambut menjadi kering. Sementara itu, jika rajin mengonsumsi lemak sehat dapat membantu memperbaiki kondisi kulit agar tetap lembap. Makanan sehat dan berlemak lainnya termasuk salmon, biji chia, minyak zaitun, dan telur.<br><br>
	6. Jaga mikrobiom kulit agar seimbang
	Cara menjaga kesehatan kulit yang ke enam adalah dengan tetap menjaga keseimbangan mikrobiom kulit. Sama seperti usus, microbiome kulit yang sehat dan seimbang sangat penting untuk menjaga kulit. Bakteri mikroskopis ini menjaga kulit tetap terhidrasi dan kenyal, melawan radikal bebas, mencegah infeksi, dan bahkan melindungi terhadap sinar UV yang berbahaya.
	Hindari sabun dan scrub yang keras, yang dapat menghilangkan minyak esensial dan bakteri yang membentuk microbiome kulit. Gunakan produk perawatan kulit yang diformulasikan khusus untuk meningkatkan microbiome kulit seperti yang mengandung pra dan 
	probiotik.<br><br>
	7. Gunakan produk perawatan kulit segera setelah mandi
	Cara menjaga kesehatan kulit yang ke tujuh adalah dengan menggunakan produk perawatan kulit seperti lotion segera sesudah mandi.
	Salah satu komponen paling penting dari kulit yang sehat adalah kelembapan. Lakukan rutinitas perawatan kulit Anda segera setelah mandi.<br><br>
	8. Rutin memijat wajah
	Cara menjaga kesehatan kulit yang ke delapan adalah dengan rutin melakukan pijat pada wajah.
	Tidak hanya terasa menyenangkan, tetapi pijatan wajah dapat mengurangi ketegangan dan meningkatkan sirkulasi, sehingga membantu menjaga sel-sel kulit tetap sehat.<br><br>
	9. Konsumsi makanan yang kaya antioksidan
	Cara menjaga kesehatan kulit yang ke sembilan adalah dengan rutin mengonsumsi makanan yang kaya akan antioksidan.
	Semakin penuh warna, semakin baik. Buah-buahan dan sayuran dianggap sebagai sumber antioksidan terbaik.<br><br>
	10. Jaga kulit tetap terhidrasi
	Cara menjaga kesehatan kulit yang terakhir adalah dengan teratur minum air putih. Minum air yang cukup sangat penting untuk kesehatan kulit secara keseluruhan.
	Menjaga tubuh tetap terhidrasi dengan baik dapat menghasilkan kulit yang lembab, sehat, dan bercahaya.<br><br>

