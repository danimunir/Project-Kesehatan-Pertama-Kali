Meningkatkan daya tahan tubuh adalah hal penting dilakukan. Sebab, secara umum daya tahan tubuh memiliki peran yang luar biasa dalam menjaga kesehatan dan bertugas untuk mencegah penyakit. Maka dari itu, penting untuk mengetahui apa saja cara yang bisa dilakukan untuk meningkatkan daya tahan tubuh, salah satunya dengan memenuhi asupan vitamin. 
<br>
Asupan vitamin untuk membantu meningkatkan daya tahan tubuh biasanya didapat dari konsumsi makanan sehat. Selain itu, memenuhi kebutuhan vitamin tubuh juga bisa dilakukan dengan mengonsumsi suplemen khusus, tentu saja yang memiliki kandungan beragam vitamin yang dibutuhkan tubuh, seperti vitamin C, E, echinacea, dan zinc.
<b>Meningkatkan Daya Tahan Tubuh setelah Vaksin Corona</b>
Bukan lagi rahasia bahwa mengonsumsi makanan sehat penting untuk menjaga kebugaran dan kondisi tubuh. Ternyata, hal itu berkaitan dengan peningkatan daya tahan tubuh yang memegang peran penting dalam hal kesehatan. Daya tahan tubuh yang terjaga tidak hanya berguna untuk menghindari penyakit, tapi juga bisa membantu memaksimalkan kinerja obat yang masuk ke dalam tubuh, termasuk vaksin. <br> 
Seperti diketahui, saat ini pandemi virus corona masih berlangsung. Kendati begitu, pemberian vaksin sebagai upaya memutus rantai penularan virus sudah dilakukan dan masih terus berlanjut. Banyak kelompok orang yang sudah menerima vaksin. Setelah masuk ke dalam tubuh, vaksin COVID-19 akan bekerja dan merangsang terbentuknya sistem pertahanan terhadap virus penyebab penyakit tertentu, dalam hal ini virus corona.
Agar kinerja vaksin maksimal, penting untuk selalu menjaga daya tahan tubuh, salah satunya dengan konsumsi vitamin. Ada beberapa jenis vitamin yang penting dikonsumsi untuk meningkatkan daya tahan tubuh setelah mendapat vaksin COVID-19, di antaranya: 
<p>1. Vitamin C </p>

Salah satu cara meningkatkan daya tahan tubuh setelah vaksin adalah mengonsumsi makanan sehat yang kaya akan kandungan vitamin C. Jenis vitamin ini bisa membantu meningkatkan kadar antibodi yang membentuk kekebalan tubuh serta bisa mempercepat penyembuhan infeksi. Kamu bisa mendapat asupan vitamin C alami dari beberapa jenis makanan, salah satunya buah-buahan. 

<p>2. Vitamin E</p>

Konsumsi makanan yang banyak mengandung vitamin E juga bisa dilakukan untuk membantu meningkatkan daya tahan tubuh setelah mendapat vaksin corona. Jenis vitamin ini kaya akan kandungan antioksidan yang bisa memperbaiki respons kekebalan tubuh dan mempercepat penyembuhan infeksi.

<p>3. Echinacea</p>

Bunga yang banyak tumbuh di Amerika Utara ini biasanya dikonsumsi sebagai teh. Namun, saat ini echinacea juga bisa ditemukan dalam bentuk suplemen atau ekstrak. Echinacea kaya akan senyawa bioaktif yang bermanfaat untuk menangkal radikal bebas, memperkuat daya tahan tubuh, menjaga kesehatan kulit, dan mengurangi kecemasan.
<p>4. Zinc</p> 
Asupan zinc dibutuhkan tubuh untuk membantu pembentukan sel imunitas atau sistem kekebalan tubuh baru. Tidak berbeda jauh dengan jenis vitamin lainnya, zinc bisa didapat dari beberapa jenis makanan, seperti yogurt, daging, dan sayuran. 
<p>Selain mengetahui jenis vitamin apa saja yang dibutuhkan untuk membantu meningkatkan daya tahan tubuh, penting juga mencari tahu dosis dari konsumsi jenis-jenis vitamin tersebut. Selain itu, kamu juga harus mengetahui apa saja efek vitamin terhadap tubuh, terutama setelah mendapatkan vaksinasi COVID-19. Dengan begitu, risiko munculnya penyakit bisa dihindari dan efek vaksin terhadap tubuh bisa dimaksimalkan.
