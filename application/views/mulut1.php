<p>A. Bagian-bagian yang terdapat dalam mulut:</p>

<h4>1. Bibir</h4>
Dua struktur seluler & otot yang membentuk pintu masuk ke mulut. Bibir tersebut menandai transisi dari kulit membran mukosa lembab.

<h4>2. Gigi (dens)</h4>
Gigi fungsinya untuk menggigit, mengunyah, mencabik. Gigi terdiri dari gigi seri, taring, susu dan geraham, sehingga makanan menjadi mudah ditelan

Lidah (lingua) Lidah ini bekerja untuk melumat makanan yang masuk serta juga sebagai indra perasa. Tanpa lidah tentu kita tidak akan pernah merasakan yang namanya rasa asin, asam, manis, serta pahit.

Lidah ini memiliki tiga (3) fungsi utama, diantaranya :

1. Sebagai indera perasa
2. Sebagai organ pencernaan untuk mencerna serta menelan makanan
3. Seabagai organ yang berperan di dalam proses berbicara
Di dalam lidah terdapat enzim amilase yaitu enzim yang mengubah zat tepung (polisakarida) menjadi zat gula (disakarida)

Gusi
Untuk bagian gusi sendiri terdiri dari jaringan fibrosa serta padat yang melapisi lengkungan alveolar dan juga pelukan gigi. Gusi ini merupakan langit-langit mulut tempat wadahnya makanan.

Langit-langit Mulut
Langit-langit mulut tersebut membentuk langit-langit mulut serta memisahkan mulut dari rongga hidung. Langit-langit ini terdiri dari 2 bagian yang sangat berbeda. Selama proses menelan tersebut, langit-langit lunak dan juga uvula bergerak ke atas supaya makanan tersebut tidak langsung masuk ke rongga hidung dan juga ke orofaring.

Kelenjar Ludah (Saliva)
Kelenjar ludah ini merupakan kelenjar kecil, ditemukan pada bagian mulut, yang menghasilkan air liur. Semuanya itu berada di bawah selaputlendir. Kelenjar ludah paling besar itu yakni kelenjar parotid yang posisinya itu pada tiap-tiap sisi, hanya di depan telinga.

Di Sekitar mulut itu terdapat Tiga (3) pasang kelenjar ludah, diantaranya  :

1. Sepasang kelenjar parotis, ini letaknya pada di bawah daun telinga <--! ini pake fungsi tag ol>
2. Sepasang kelenjar sublingualis, ini letaknya pada di bawah lidah
3. Sepasang kelenjar submandibularis, ini letaknya pada di sekitar tulang mandibula.

Fungsi Mulut
Dibawah ini merupakan fungsi mulut manusia antara lain sebagai berikut :
<--! ini pake fungsi tang ol>
1.	Sebagai alat berbicara.
2.	Menjadi salah satu bagian dari sistem pencernaan
2.	Menjadi tempat keluarnya nafas selain dari hidung.
4.	Sebagai tempat mengunyah dan juga menggiling makanan
5.	Merasakan makanan
6.	Menelan makanan
7.	Tempat pertama bagi makanan yang masuk pada pencernaan

Gangguan Mulut
Dibawah ini adalah beberapa gangguan/ penyakit dari mulut antara lain sebagai berikut :

1.	Halitosis (bau mulut)
2.	Sindrom mulut kering
3.	ulkus mulut
4.	kanker mulut
5.	Sumbing serta bibir sumbing
6.	Infeksi seperti sariawan, herpes (sakit sariawan) atau juga tonsilitis
7.	Masalah Tongue seperti misalnya permukaan retak atau dilapisi
8.	Masalah gigi seperti misalnya gigi berlubang atau juga dampak gigi bungsu

Demikianlah penjelasan mengenai Pengertian Mulut, Struktur, Bagian, Fungsi dan Gangguannya, semoga apa yang diuraikan dapat bermanfat untuk anda

