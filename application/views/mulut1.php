 			<p>Mulut adalah suatu rongga terbuka tempat masuknya makanan dan air pada hewan. Mulut biasanya terletak di kepala dan umumnya merupakan bagian awal dari sistem pencernaan lengkap yang berakhir di anus. Mulut terdiri dari gigi dan lidah.</p>
            

 			
