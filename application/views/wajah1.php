 			Masker wajah adalah masker kecantikan yang berwujud sediaan gel,
pasta dan serbuk yang dioleskan untuk membersihkan dan mengencangkan
kulit, terutama kulit wajah. Secara sistematik, masker wajah bertindak
merangsang sirkulasi aliran darah maupun limpa, merangsang dan
memperbaiki kulit melalui percepatan proses regenerasi dan memberikan
nutrisi pada jaringan kulit. Masker wajah juga berfungsi sebagai pembawa
bahan-bahan aktif yang berguna bagi kesehatan kulit, seperti ekstrak
tumbuhan, minyak esensial, atau rumput laut yang dapat diserap oleh
permukaan kulit untuk dibawa ke dalam sirkulasi darah
				<br>
				Masker wajah saat ini memiliki banyak bentuk seperti serbuk, pasta, ada
juga yang berbentuk gel. Berbagai perusahaan kosmetik besar saat ini banyak
mengeluarkan produk yang berbahan alami atau “back to nature”. Masyarakat
saat ini banyak yang beralih pada produk yang berbahan alami, keistimewaan
masker dari bahan alami ini adalah tidak menimbulkan iritasi dan efek
samping. Karena produk yang terbuat dari bahan alamiah lebih murah, aman,
tidak menimbulkan efek samping yang membahayakan bagi kulit 
<br><br>
				<b>Jenis-jenis masker wajah yang bagus untuk digunakan</b><br>
				<b>1.Sheet Mask</b>
				yaitu suatu masker yang berbentuk lembaran tisu atau kapas dengan lubang di bagian mata, hidung, dan bibir. Sheet mask sangat baik bagi Anda yang memiliki kulit kering, walau semua jenis kulit juga bisa mendapatkan manfaat dengan memakainya.

Cara penggunaannya juga mudah. Anda cukup menempelkan masker wajah ini di kulit setelah mencuci muka, lalu diamkan selama 15–20 menit agar kandungannya dapat meresap ke dalam kulit.
				<br>
				<b>2. Sleeping Mask</b>
				Sama halnya dengan sheet mask, sleeping mask juga lebih dahulu populer di negara Korea Selatan. Sesuai namanya, masker ini digunakan sebelum tidur.

Teksturnya berbentuk krim atau gel yang digunakan dengan cara dioleskan ke seluruh kulit wajah. Tunggu sampai masker diserap kulit dan mengering dengan sendirinya. Masker biasanya baru dibersihkan keesokan paginya.
				<br>
				<b>3. Clay Mask</b>
				yaitu jenis masker wajah berbahan dasar tanah liat dengan kandungan mineral yang bermanfaat bagi kulit.
				Setelah mencuci muka dan mengeringkannya, kemudain oleskan clay mask ke seluruh wajah, lalu tunggu hingga beberapa saat hingga masker wajah tersebut mengering. Setelah itu, Anda bisa membersihkan masker wajah ini dengan handuk atau sponge wajah yang sudah dicelupkan ke air hangat.
				<br>
				<b>4. Peel Off Mask</b>
				yaitu jenis masker yang berbentuk gel atau krim dan biasanya akan mengering dalam waktu beberapa menit setelah dioleskan pada kulit wajah. Ketika sudah kering, masker wajah jenis ini akan berubah teksturnya menjadi seperti karet yang elastis saat dikelupas.

Peel off mask biasa digunakan untuk mengangkat komedo yang membandel. Namun, masker ini juga memiliki kemampuan mengangkat minyak, kotoran, dan sel kulit mati, tergantung pada kandungannya.
