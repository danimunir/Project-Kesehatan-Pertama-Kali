				 Mengkonsumsi gula dan garam yang tinggi dan kurangnya nutrisi dapat melemahkan sistem kekebalan kekebalan tubuh.Selain itu makan makanan yang berlemak tinggi juga bisa membuat kamu kelebihan berat badan. Kelebihan berat badan bahkan sampai obesitas bisa membuat kamu lebih rentan terhadap penyakit.
				<br><br>
				<b>Cara meningkatkan daya tahan tubuh</b><br>
				<b>1. Mengonsumsi makanan kaya akan vitamin dan mineral</b>
				Kekuatan imunitas secara menyeluruh tergantung pada pasokan vitamin dan mineral yang optimal, terutama yaitu Vitamin A sebagai pendongkrak pertahanan tubuh dengan cepat. Selain itu, sejumlah mineral juga dikaitkan dengan antioksidan dan telah terbukti dapat memengaruhi daya tahan tubuh secara positif, di antaranya yaitu Zat besi,Mangan, Tembaga, Selenium.
				<br>
				<b>2. Menjaga kebersihan makanan</b>
				Kebersihan makanan perlu diperhatikan, mulai dari proses pembersihan, pembuatan, termasuk juga perlengkapan makan dan air minum untuk menunjang daya tahan tubuh dan kesehatan.
				<br>
				<b>3. Menjaga asupan nutrisi dengan baik</b>
				Daya tahan tubuh dapat ditingkatkan dengan sangat efektif melalui kombinasi nutrisi, yaitu nutrisi makro, serat dan nutrisi mikro. Nutrisi makro, di antaranya yakni karbohidrat, protein dan lemak. Sedangkan nutrisi mikro adalah vitamin dan mineral.
				<br>
				<b>4. Rajin berolahraga</b>
				 olahraga dapat merangsang kerja antibodi dan sel-sel darah putih, yaitu sel kekebalan tubuh yang berfungsi melawan berbagai kuman penyebab penyakit. Berkat olahraga, sel darah putih dapat bersirkulasi lebih lancar sehingga lebih cepat mendeteksi keberadaan penyakit.Manfaat lain berolahraga adalah membantu mengeluarkan bakteri dari paru-paru dan saluran pernapasan. Ini yang membuat orang yang rajin berolahraga tidak mudah terkena flu atau penyakit pernapasan lainnya.
