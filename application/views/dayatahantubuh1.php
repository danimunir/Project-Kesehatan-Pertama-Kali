				<b>2. Mengonsumsi minuman beralkohol</b>
				terlalu sering atau secara berlebihan bisa memberikan efek negatif pada sistem kekebalan tubuh. Minum terlalu banyak dapat menghilangkan lapisan mulut dan kerongkongan, sehingga  membuat virus dan bakteri mudah memasuki tubuh.
				<br>
				<b>3. Stres</b>
				Tingkat stres yang tinggi dapat melemahkan respons kekebalan tubuh, sehingga bisa rentan terserang flu dan infeksi virus lainnya.
