 		<div class="text">  

 			Mulut yang selama ini kita anggap hanya sebagai tempat mengunyah dan merasakan nikmatnya makanan,
	 ternyata ia berpotensi memiliki segudang masalah atau memiliki jenis-jenis penyakit mulut yang dapat mengganggu aktifitas pencernaan.
Diantara jenis jenis penyakit mulut yang perlu kita waspadai yakni:

<p>1. Bau mulut</p>

Penyakit yang sebenarnya sering kita jumpai dan seringkali dianggap remeh ialah bau mulut. Bau mulut yang dimiliki seseorang seringkali menunjukkan kondisi kesehatan mulutnya secara umum. Misalnya, bau mulut dapat disebabkan karena adanya masalah di dalam mulut seperti: gigi dan atau gusi yang  kurang bersih, sariawan, luka pada mulut, infeksi, konsumsi makanan tertentu yang menimbulkan bau ketika belum dibersihkan dan gigi palsu yang tidak terawat. Maka dari itu, anda harus tahu cara mencegah bau mulut dan memilih makanan serta minuman yang sehat untuk mulut anda. Karena makanan sehat bukan hanya enak di mulut, namun juga menyehatkan mulut.

<p>2. Sariawan</p>

Penyakit sariawan dianggap remeh oleh sebagian orang, bahkan ketika seseorang terserang sariawan mereka tidak akan segera mengobatinya karena dianggap remeh. Penyakit ini dapat menyerang bayi, anak-anak sampai orang-orang dewasa sekalipun. Sariawan timbul dengan sebab yang beragam. Penyebab sariawan ada yang dipicu oleh benturan/ gesekan di luar maupun dalam mulut sehingga menimbulkan luka, ada pula sariawan yang timbul disebabkan oleh jamur maupun bakteri.

<p>3. Radang  mulut</p>

Penyakit ini disebabkan oleh infeksi jamur. Beberapa ciri ketika seseorang diserang penyakit ini ialah lidah yang  berwarna pucat dan terdapat bercak kuning keputihan yang bisa dikeruk dengan mudah. Rasa perih dari bercak tersebut biasanya akan diderita oleh pasien jika terkena makanan atau saat menyikat gigi.

<p>4. Glossitis</p>

Dari beberapa jenis jenis penyakit mulut, penyakit yang satu ini mungkin terbilang asing. Penyakit yang punya nama glossitis ini dapat membuat lidah penderita terasa sakit karena adanya rekahan. Kondisi ini terjadi akibat jaringan pelindung di permukaan lidah tidak terbentuk secara sempurna. Biasanya disebabkan oleh tubuh kita dari dalam, bukan karena adanya kontak fisik. Misalnya, disebabkan oleh tubuh yang sedang mengalami anemia atau sedang kekurangan vitamin tertentu.

<p>5. Luka busuk</p>

Pemicu dari penyakit yang satu ini ialah termasuk hipersensitivitas, infeksi, hormon, stres, dan tidak mendapatkan beberapa vitamin yang cukup. Luka busuk juga disebut borok aphthous, sariawan yang bisa muncul di lidah, pipi, bahkan gusi anda dan menjadi parah atau bisa dikatakan membusuk. Kita tahu bahwa luka luar saja yang berada di bagian tubuh kita yang kering dan terkena angina/ udara, bisa menimbulkan bau jika membusuk.

<p>Maka, apalagi luka yang berada pada baian tubuh kita yang basah dan jarang terkena udara. Maka penyakit ini selain membuat anda tidak nyaman, tetapi juga sangat mampu menimbulkan bau mulut. Keadaan mulut yang kurang bersih, juga dapat memperparah keadaannya.</p>

<p>6. Bibir dan mulut yang perih</p>

Waspadai penyakit syphilis yang tertular akibat oral seks yang bisa menyebabkan mulut terasa perih. Keluhan perih ini biasanya akan hilang setelah beberapa hari. Namun jika terasa perih lebih dari 3 minggu, bisa saja pertanda adanya kanker. Maka jika anda menemui tanda-tanda tersebut, mengkonsultasikannya ke dokter dengan segera mungkin akan sangat membantu.

<p>7. Infeksi gusi</p> 

Kondisi meningkatnya peradangan dapat menyebabkan gusi menyusut, membentuk kantong diantara gigi dan gusi. Kantong ini dipicu oleh karang gigi, plak dan sisa-sisa makanan yang pada akhirnya menyebabkan infeksi dan abses. Penyakit gusi ini selanjutnya dapat merusak tulang yang mendukung gigi, serta penyakit ini merupakan salah satu penyebab utama hilangnya gigi pada orang dewasa.

<p>Meski penyakit ini secara langsung terdapat pada gusi, namun secara tidak langsung penyakit ini juga disebabkan oleh kodisi gigi yang tidak sehat. Untuk itu kita juga perlu menjaga kesehatan gigi salah satunya dengan mengenal jenis jenis penyakit gigi yang harus anda cegah.</p>

<p>8. Infeksi Virus Herpes Simpleks</p>

Penyakit ini memiliki gejala adanya titik-titik berwarna kuning pucat di lidah, selain itu suhu badan juga tinggi hingga dapat mencapai 38 derajat celcius atau lebih, terkadang muncul rasa nyeri hingga perih di sekitar bibir, diawali dengan lepuhan yang sangat sakit. Jika anda memiliki daya tahan tubuh yang kuat, gejala ini akan hilang dalam beberapa hari. Namun jika sebaliknya, keadaan ini bisa saja semakin parah. Maka penanganan dokter sangat diperlukan.

<p>9. Coxsackle Virus</p>

Diantara jenis jenis penyakit mulut, virus coxsackle ini termasuk yang biasanya lebih suka menyerang anak-anak. Selain pada mulut, titik-titik merah yang disebabkan virus ini dapat menyerang di kaki dan tangan. Terkadang juga diiringi demam dan mengigil oleh penderita. Ada istilah lain yang biasa dipakai untuk menyebut penyakit ini, yaitu sindrom tangan, kaki dan mulut.

<p>Karena virus ini menular, maka lebih baik anak/ penderita diisolasi untuk mengurangi kemungkinan penularannya terhadap orang lain. Dengan memberikan makanan tinggi protein dan istirahat yang cukup pada, maka daya tahan tubuhnya akan meningkat untuk dapat membantu proses penyembuhan dengan cepat.</p>

<p>Ada sekitar 9 resiko penyakit mulut yang dapat mengancam disekitar kita, atau bahkan bisa lebih dari itu. Dari jenis jenis penyakit mulut yang ada, selain virus dan kebersihan mulut, makanan yang kita konsumsi dan daya tahan tubuh kita juga sangat menentukan seberapa kuat kita melawan ancaman jenis jenis penyakit mulut tersebut. Maka dari itu, kebersihan mulut dan segala hal yang menyangkut kesehatan mulut kita, harus sangat diperhatikan.</p>

<p>Proses pencegahan misalnya dapat dilakukan dengan memakan makanan yang menyehatkan gigi, dan mengetahui tips merawat gigi behel bagi anda yeng menggunakannya, karena kesehatan mulut anda sangat ditunjang oleh kesehatan gigi anda. Selain itu hal penting yang juga harus anda tahu demi kesehatan mulut. Bagi yang berniat mencabut gigi ketahuilah dulu alasan gigi harus dicabut, karena jika anda salah memutuskan sesuatu, ini juga akan menyangkut kesehatan mulut anda.
 Jadi, berhati-hatilah agar masalah mulut tidak mengganggu aktifitas pencernaan anda.</p>

