 		<div class="text"><h1> Jenis-Jenis Penyakit Mulut Dan Cara Menghindarinya </h1>
 			<p>Mulut merupakan tempat utama penerima asupan makanan, dimana makanan di masukan ke dalam mulut dan dihaluskan sehingga masuk ke organ-organ yang lainnya. </p>
            

 			
