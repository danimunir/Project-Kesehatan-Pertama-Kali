 		<div class="text">  <p>Beberapa kondisi mata yang umum meliputi:</p>
 Mata merah muda yang juga dikenal sebagai konjungtivitis mata kering yang terjadi ketika saluran air mata tidak dapat melumasi mata dengan baik Blepharitis atau suatu kondisi yang menyebabkan kelopak mata meradang dan mengeras Bintitan Keratitis, infeksi pada kornea.
Berikut ini adalah beberapa cara mengobati infeksi mata secara alami yang tersedia
<p>1.Air garam  </p>
Melansir Health Line, air garam atau cairan saline adalah salah satu pengobatan rumahan yang paling efektif untuk infeksi mata. Saline sendiri mirip dengan air mata, yang termasuk dalam bagian dari cara mata untuk membersihkan dirinya sendiri secara alami.Garam juga memiliki sifat antimikroba. Karena itu, masuk akal bahwa garam dapat mengobati infeksi mata secara efektif. Cairan saline steril dapat dibeli di apotek atau bisa juga secara online.
<p>2. Kantong teh </p>
Menempelkan kantong teh dingin di mata Anda saat ditutup bisa menjadi cara untuk bersantai dan melepas lelah. Beberapa orang mengatakan bahwa ini bisa menjadi pengobatan rumahan yang efektif untuk infeksi mata. Beberapa jenis teh memiliki sifat anti-inflamasi yang menenangkan. Misalnya, sebuah penelitian menunjukkan bahwa teh hijau, teh chamomile, rooibos, dan teh hitam, semuanya memiliki sifat anti-inflamasi.Oleh karena itu, menggunakan kantong teh di mata bisa menjadi cara efektif untuk mengurangi pembengkakan. Tapi memang, sejauh ini belum ada penelitian yang menunjukkan bagaimana kantong teh dapat memengaruhi mata, atau apakah dapat digunakan untuk mengobati infeksi mata.Ingatlah juga bahwa meskipun perawatan antiradang dapat meredakan gejalanya, infeksi mata harus ditangani penyebabnya.
<p>3. Kompres hangat </p>
Jika mata Anda sakit, terinfeksi, atau teriritasi, kompres hangat dapat membantu meredakan keluhan. Sebuah studi pada 2014 terhadap 22 partisipan menyarankan bahwa kompres hangat dapat meningkatkan kesehatan mata pada mereka yang memiliki mata sehat. Sebuah tinjauan pada 2012 dari beberapa penelitian menunjukkan bahwa kompres hangat dapat membantu penderita blepharitis, suatu kondisi yang menyebabkan kelopak mata menjadi meradang dan mengeluarkan kotoran (belek). Selain itu, American Academy of Ophthalmology menyarankan penggunaan kompres hangat untuk meredakan gejala mata merah.

<p>4. Kompres dingin</p> 
Seperti kompres hangat, kompres dingin tidak dapat menyembuhkan infeksi mata. Namun, kompres dingn dapat meredakan ketidaknyamanan yang terkait dengan penyakit mata tertentu. Baca juga: Alasan Air Hangat Lebih Tepat untuk Mengompres Anak Demam Kompres dingin dapat mengurangi pembengkakan jika terjadi cedera mata dan infeksi. Berikut tips membuat kompres dingin: Rendam kain dalam air dingin dan tempelkan dengan lembut pada mata Anda juga dapat mendinginkan kain basah di dalam kantong plastik yang dapat ditutup selama beberapa menit sebelum digunakan pada mata Jangan pernah menekan mata dengan kuat atau meletakkan es langsung di mata atau kelopak mata agar tidak malah menimbulkan masalah 
<p>5. Cuci perlengkapan pribadi</p>
 Cuci handuk dan sarung bantal setiap hari saat Anda mengalami infeksi mata, seperti konjungtivitis. Hal itu dikarenakan, benda-benda ini dapat bersentuhan dengan mata yang terinfeksi, sehingga barang-barang tersebut bisa menyebarkan infeksi ke mata lain atau menyebabkan orang lain di keluarga terkena infeksi. Gunakan air panas dan deterjen untuk membunuh bakteri yang tersisa dalam peralatan pribadi.
<p>6. Obat tetes mata </p>
Melansir Medical News Today, obat tetes mata atau "natural tears" dapat digunakan untuk meredakan iritasi atau rasa terbakar di mata. Obat tetes mata dapat membantu meringankan gejala semua jenis infeksi mata yang menular. Obat tetas mata bisa membantu menghilangkan alergen, iritan, dan kotoran.
<p>7. Obat pereda nyeri</p>
 Obat tetes mata yang mengandung antihistamin dapat membantu meredakan nyeri yang berhubungan dengan infeksi mata menular. Beberapa obat yang dijual bebas dapat membantu meminimalkan gejala mata merah. Obat antiinflamasi nonsteroid (NSAID), seperti ibuprofen, dapat membantu meredakan nyeri dan peradangan. Obat alergi juga dapat meredakan gejala penyakit mata yang menular jika penyebabnya adalah alergi. <br> 
Obat-obatan ini meliputi: <br>
•	Tablet antihistamin <br>
•	Obat tetes mata topikal yang mengandung antihistamin <br>
•	Obat stabilisator sel mast (mast cell stabilizers) <br>

