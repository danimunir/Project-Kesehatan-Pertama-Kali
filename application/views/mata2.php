 		<div class="text"><h1> Cara Mengobati Infeksi Mata </h1>
 			Mata adalah organ penglihatan. Mata mendeteksi cahaya dan mengubahnya menjadi impuls elektrokimia pada sel saraf. Pada organisme yang lebih tinggi, mata adalah sistem optik kompleks yang mengumpulkan cahaya dari lingkungan sekitarnya, mengatur intensitasnya melalui diafragma, memfokuskan melalui penyesuaikan lensa untuk membentuk sebuah gambar, mengkonversi gambar tersebut menjadi satu himpunan sinyal listrik, dan mentransmisikan sinyal-sinyal ke otak melalui jalur saraf kompleks yang menghubungkan mata melalui saraf optik menuju korteks visual dan area lain dari otak. Maka dari itu kita harus menjaga kesehatan Mata agar terhindar dari iritasi dan sebagai nya.
            

 			
